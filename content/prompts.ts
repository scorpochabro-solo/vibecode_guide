export const ALL_PROMPTS: PromptAsset[] = [
  GENERATOR_SYSTEM_PROMPT,
  MASTER_TEMPLATE_PROMPT,
  CLAUDE_MD_TEMPLATE,
  AGENTS_MD_TEMPLATE,
  ...PROMPT_LIBRARY,
];

export function getPrompt(id: string): PromptAsset {
  const found = ALL_PROMPTS.find((p) => p.id === id);
  if (!found) throw new Error(`Prompt not found: ${id}`);
  return found;
}
